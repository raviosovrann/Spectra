// Generate price history data
export const generatePriceHistory = (basePrice: number, days: number = 30) => {
  const data = []
  let price = basePrice * 0.85 // Start 15% lower
  const now = Date.now()

  for (let i = days; i >= 0; i--) {
    const change = (Math.random() - 0.48) * (basePrice * 0.03) // Slight upward bias
    price = Math.max(price + change, basePrice * 0.7) // Don't go below 30% of base
    data.push({
      timestamp: now - i * 24 * 60 * 60 * 1000,
      price: parseFloat(price.toFixed(2)),
    })
  }

  return data
}
